"""
Module defining the CLI color theme.
"""


class Theme:
    """Centralized CLI color theme constants."""

    GREEN: str = '\033[32m'
    PURPLE: str = '\033[35m'
    YELLOW: str = '\033[33m'
    RED: str = '\033[31m'
    DARK_GREY: str = '\033[90m'
    CYAN: str = '\033[36m'
    RESET: str = '\033[0m'
